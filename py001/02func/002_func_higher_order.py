#!/usr/bin/env python
# -*- coding: utf-8 -*-

#=====================函数高级特性==========================
# 函数的调用可以赋值给变量
x = abs(-10)

# 函数名可以赋值给变量
f = abs
print(f"这是一个函数被赋值后的调用f(-10) => {f(-10)}")

# 函数可以作为参数传递, 一个函数接收另外一个参数作为参数，这个函数就称为高阶函数
def add(x, y, f):
    return f(x) + f(y)

# 这是一个高阶函数的调用add(-5, 6, abs) => 11
print(f"这是一个高阶函数的调用add(-5, 6, abs) => {add(-5, 6, abs)}")
